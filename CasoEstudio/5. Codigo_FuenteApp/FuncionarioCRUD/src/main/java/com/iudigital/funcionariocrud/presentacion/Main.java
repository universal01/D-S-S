    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        
        if(txtNumero_Doc.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite numero de documento");
            txtNumero_Doc.requestFocus();
            return;
        }
        
        if(txtNombres.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite nombres");
            txtNombres.requestFocus();
            return;
        }
        
        if(txtApellidos.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite apellidos");
            txtApellidos.requestFocus();
            return;
        }
        
        if(txtEstado_civil.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite estado civil");
            txtEstado_civil.requestFocus();
            return;
        }
        
        if(txtSexo.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Sexo");
            txtSexo.requestFocus();
            return;
        }
        
        if(txtDireccion.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite direccion");
            txtDireccion.requestFocus();
            return;
        }
        
        if(txtTelefono.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Telefono");
            txtTelefono.requestFocus();
            return;
        }
        
        if(txtFecha_Nac.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Fecha de nacimiento");
            txtFecha_Nac.requestFocus();
            return;
        }
        
        Funcionario funcionario = new Funcionario();
        funcionario.setNumeroIdentificacion(txtNumero_Doc.getText().trim());
        funcionario.setNombres(txtNombres.getText().trim());
        funcionario.setApellidos(txtApellidos.getText().trim());
        funcionario.setEstadoCivil(txtEstado_civil.getText().trim());
        funcionario.setSexo(txtSexo.getText().trim());
        funcionario.setDireccion(txtDireccion.getText().trim());
        funcionario.setTelefono(txtTelefono.getText().trim());
        funcionario.setFechaNacimiento(LocalDate.MIN);
        try {
            funcionarioController.createOneFuncionario(funcionario);
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtNumero_Doc.setText("");
        txtNombres.setText("");
        txtApellidos.setText("");
        txtEstado_civil.setText("");
        txtSexo.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
        txtFecha_Nac.setText("");
        listFuncionarios();
        JOptionPane.showMessageDialog(null, "Funcinanrio creado con Ã©xito");
    }//GEN-LAST:event_btnCreateActionPerformed

