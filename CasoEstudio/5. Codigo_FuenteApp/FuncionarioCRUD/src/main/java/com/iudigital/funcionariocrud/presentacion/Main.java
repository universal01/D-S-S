    private void listFuncionarios() {
        cbxFuncionario_id.removeAllItems();
        Funcionario funcionario1 = new Funcionario();
        funcionario1.setNombres(SELECCIONE);
        funcionario1.setApellidos("");
        cbxFuncionario_id.addItem(funcionario1);

        DefaultTableModel defaultTableModel = new DefaultTableModel();
        for (String COLUMN : COLUMNS) {
            defaultTableModel.addColumn(COLUMN);
        }

        tblFuncionarios.setModel(defaultTableModel);

        try {

            List<Funcionario> funcionarios = funcionarioController.getFuncionarios();
            if (funcionarios.isEmpty()) {
                return;
            }
            defaultTableModel.setRowCount(funcionarios.size());
            int row = 0;
            for (Funcionario funcionario : funcionarios) {
                defaultTableModel.setValueAt(funcionario.getIdFuncionario(), row, 0);
                defaultTableModel.setValueAt(funcionario.getNumeroIdentificacion(), row, 1);
                defaultTableModel.setValueAt(funcionario.getNombres(), row, 2);
                defaultTableModel.setValueAt(funcionario.getApellidos(), row, 3);
                defaultTableModel.setValueAt(funcionario.getEstadoCivil(), row, 4);
                defaultTableModel.setValueAt(funcionario.getSexo(), row, 5);
                defaultTableModel.setValueAt(funcionario.getDireccion(), row, 6);
                defaultTableModel.setValueAt(funcionario.getTelefono(), row, 7);
                defaultTableModel.setValueAt(funcionario.getFechaNacimiento(), row, 8);
                row++;

                cbxFuncionario_id.addItem(funcionario);

            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    private void addListener() {
        cbxFuncionario_id.addItemListener((ItemEvent event) -> {
        Funcionario selectedFuncionario = (Funcionario) event.getItem();
        if (selectedFuncionario.getNombres().equals(SELECCIONE)) {
            txtApellidos_edit.setText("");
            txtEstado_civil_edit.setText("");
            txtSexo_edit.setText("");
            txtDireccion_edit.setText("");
            txtTelefono_edit.setText("");
            txtFecha_nac_edit.setText("");
        } else {
            txtNombres_edit.setText(String.valueOf(selectedFuncionario.getNombres()));
            txtNumero_Doc_edit.setText(selectedFuncionario.getNumeroIdentificacion());
            txtNombres_edit.setText(selectedFuncionario.getNombres());
            txtApellidos_edit.setText(selectedFuncionario.getApellidos());
            txtEstado_civil_edit.setText(selectedFuncionario.getEstadoCivil());
            txtSexo_edit.setText(selectedFuncionario.getSexo());
            txtDireccion_edit.setText(selectedFuncionario.getDireccion());
            txtTelefono_edit.setText(selectedFuncionario.getTelefono());
            txtFecha_nac_edit.setText(String.valueOf(selectedFuncionario.getFechaNacimiento()));

            
         }
        });
    }
    

